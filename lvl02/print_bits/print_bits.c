#include <unistd.h>
#include <stdio.h>

void	print_bits(unsigned char octet)
{
	int	z;
	int	o;

	z = 128;
	o = octet;
	while (z > 0)
	{
		if (z & o)
			write(1, "1", 1);
		else
			write(1, "0", 1);
		z >>= 1;
	}
}
