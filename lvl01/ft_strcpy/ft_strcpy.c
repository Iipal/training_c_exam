#include "../../includes/lft_str.h"

char	*ft_strcpy(char *dest, const char *str)
{
	size_t	i;

	i = -1;
	while (str[++i] != '\0')
		dest[i] = str[i];
	dest[i] = '\0';
	return (dest);
}
